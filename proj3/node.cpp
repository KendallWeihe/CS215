#include <iostream>
#include <string>
#include "node.h"

using namespace std;

//-------------------------------------------------------------------------------------
//                                Node() -- default constructor
//-------------------------------------------------------------------------------------
Node::Node(){
  key = 0;
  data = "";
  left = NULL;
  right = NULL;
}

//-------------------------------------------------------------------------------------
//                                Node() -- constructor
//-------------------------------------------------------------------------------------
Node::Node(int k, string d){
  key = k;
  data = d;
  left = NULL;
  right = NULL;
}

//-------------------------------------------------------------------------------------
//                                ~Node() -- destructor
//-------------------------------------------------------------------------------------
Node::~Node(){
  if (left != NULL)
    delete left;
  if (right != NULL)
    delete right;
}

//-------------------------------------------------------------------------------------
//                                getKey()
//-------------------------------------------------------------------------------------
int Node::getKey(){
  return key;
}

//-------------------------------------------------------------------------------------
//                                getData()
//-------------------------------------------------------------------------------------
string Node::getData(){
  return data;
}

//-------------------------------------------------------------------------------------
//                                getLeft()
//-------------------------------------------------------------------------------------
Node *Node::getLeft(){
  if (left == NULL){
    return nullptr;
  }
  else {
    return left;
  }
}

//-------------------------------------------------------------------------------------
//                                getRight()
//-------------------------------------------------------------------------------------
Node *Node::getRight(){
  if (right == NULL){
    return nullptr;
  }
  else {
    return right;
  }
}

//-------------------------------------------------------------------------------------
//                                setKey()
//-------------------------------------------------------------------------------------
void Node::setKey(int k){
  key = k;
}

//-------------------------------------------------------------------------------------
//                                setData()
//-------------------------------------------------------------------------------------
void Node::setData(string d){
  data = d;
}

//-------------------------------------------------------------------------------------
//                                setLeft()
//-------------------------------------------------------------------------------------
void Node::setLeft(Node *l){
  left = l;
}

//-------------------------------------------------------------------------------------
//                                setRight()
//-------------------------------------------------------------------------------------
void Node::setRight(Node *r){
  right = r;
}
